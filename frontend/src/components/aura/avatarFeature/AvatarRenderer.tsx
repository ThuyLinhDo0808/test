import React from 'react';
import { Group } from 'three';
import { GLTFResult } from '@/types/GLTFtypes';

interface AvatarRendererProps {
  group: React.RefObject<Group | null>;
  nodes: GLTFResult['nodes'];
  materials: GLTFResult['materials'];
}

